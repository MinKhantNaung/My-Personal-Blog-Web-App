    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $image = Image::find($id);
        $oldImage = $image->name;
        Storage::delete('public/social_images/' . $oldImage);
        $image->delete();

        return back()->with('successMsg', 'Successfully deleted an image!');
    }
