    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'image' => 'required|file|image|mimes:jpg,jpeg,png,webp,svg',
        ]);

        $imageName = uniqid() . '_' . $request->file('image')->getClientOriginalName();
        $request->file('image')->storeAs('public/social_images/', $imageName);

        Image::create([
            'name' => $imageName,
        ]);

        return redirect()->route('images.index')->with('successMsg', 'Successfully created image!');
    }

